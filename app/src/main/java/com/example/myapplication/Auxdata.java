package com.example.myapplication;

public class Auxdata {

    private String info;
    private String img;

    public Auxdata(String info, String img) {
        this.info = info;
        this.img = img;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }
}
